"""
Generate OEIS b-file terms and verify code samples for T(n,k) = n + Omega*(k).
Triangle read by rows: n=1,2,...; k=1..n with gcd(n,k)=1.
"""

from math import gcd
from sympy import factorint

# ── Core function ────────────────────────────────────────────────────────────

def omega_star(n):
    if n <= 1: return 0
    return sum(e * (p - 1) for p, e in factorint(n).items())

def T(n, k):
    assert gcd(n, k) == 1, f"gcd({n},{k}) != 1"
    return n + omega_star(k)

# ── Generate flat sequence ────────────────────────────────────────────────────

def generate(max_n):
    seq = []
    for n in range(1, max_n + 1):
        for k in range(1, n + 1):
            if gcd(n, k) == 1:
                seq.append((n, k, T(n, k)))
    return seq

terms = generate(50)
values = [v for _, _, v in terms]

print("=" * 65)
print(f"Total terms up to n=50: {len(terms)}")
print()

# First 80 terms
print("First 80 terms (for OEIS sequence field):")
print(", ".join(map(str, values[:80])))
print()

# Row structure
print("Triangle by rows (n=1..12):")
for n in range(1, 13):
    row = [T(n,k) for k in range(1, n+1) if gcd(n,k)==1]
    ks  = [k     for k in range(1, n+1) if gcd(n,k)==1]
    print(f"  n={n:2d}: {row}  (k={ks})")

print()
print("Row lengths (= phi(n) for n>=2, 1 for n=1):")
from sympy import totient
