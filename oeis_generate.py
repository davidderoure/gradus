# ── Cross-check: alternative Omega* formulas ─────────────────────────────────

print("=" * 65)
print("FORMULA CROSS-CHECK")
print("  Omega*(n) = A001414(n) - A001222(n) = sopfr(n) - bigomega(n)")
print("=" * 65)

def sopfr(n):
    """Sum of prime factors with repetition (A001414)."""
    if n <= 1: return 0
    return sum(p * e for p, e in factorint(n).items())

def bigomega(n):
    """Number of prime factors with multiplicity (A001222)."""
    if n <= 1: return 0
    return sum(e for e in factorint(n).values())

print("\n  n   Omega*(n)   sopfr-bigomega   match?")
for n in range(1, 21):
    os = omega_star(n)
    alt = sopfr(n) - bigomega(n)
    ok = "✓" if os == alt else "✗"
    print(f"  {n:2d}  {os:9d}  {alt:15d}   {ok}")

